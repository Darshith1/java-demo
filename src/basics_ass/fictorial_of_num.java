package basics_ass;

public class fictorial_of_num {
	public static void main(String[] args) {
		int max = 0;
		for(int i=1; i<=5; i++) { 
			int fact = 0;
			for(int j=1; j<=i; j++) {
				if(i%j==0);
				fact++;
			} 
			if(fact>=max)
				max = fact;{
			System.out.println(max);
		}
	}
	}
}
